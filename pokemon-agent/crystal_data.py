def get_move_category(move_type: str) -> str:
    """Get move category from type (Gen 2 physical/special split)."""
    if move_type in _PHYSICAL_TYPES:
        return "physical"
    return "special"


def get_move_info(move_id: int) -> Tuple[str, str, int, int, str]:
    """Get (name, type, power, accuracy, category) for a move ID."""
    name = MOVE_NAMES.get(move_id, f"move#{move_id}")
    if move_id in MOVE_DATA:
        mtype, power, acc = MOVE_DATA[move_id]
        cat = "status" if power == 0 and acc == 0 else get_move_category(mtype)
        return name, mtype, power, acc, cat
    return name, "Normal", 0, 100, "status"



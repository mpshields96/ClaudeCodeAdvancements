# ---------------------------------------------------------------------------
# Export: CSS custom properties
# ---------------------------------------------------------------------------

def to_css_vars() -> str:
    """Generate CSS custom properties for all tokens."""
    lines = [":root {"]

    # Colors
    for name, val in CCA_PALETTE.items():
        lines.append(f"  --cca-{name}: {val};")

    # Dark palette
    for name, val in DARK_PALETTE.items():
        lines.append(f"  --cca-{name.replace('_', '-')}: {val};")

    # Series
    for i, color in enumerate(SERIES_COLORS):
        lines.append(f"  --cca-series-{i + 1}: {color};")

    # Spacing
    for name, val in _SPACING_TOKENS.items():
        lines.append(f"  --space-{name}: {val}px;")

    # Typography
    for role, spec in TYPOGRAPHY.items():
        lines.append(f"  --type-{role}-size: {spec['size_pt']}pt;")
        lines.append(f"  --type-{role}-weight: {spec['weight'].lower()};")
        lines.append(f"  --type-{role}-lh: {spec['line_height']};")

    lines.append("}")
    return "\n".join(lines)

# ---------------------------------------------------------------------------
# Export: Typst variables
# ---------------------------------------------------------------------------

def to_typst_vars() -> str:
    """Generate Typst variable definitions for all tokens."""
    lines = ["// CCA Design Tokens — auto-generated by design_tokens.py", ""]

    # Colors
    for name, val in CCA_PALETTE.items():
        lines.append(f'#let cca-{name} = rgb("{val}")')

    lines.append("")

    # Fonts
    lines.append('#let cca-font-body = "Source Sans 3"')
    lines.append('#let cca-font-code = "Source Code Pro"')
    lines.append("")

    # Spacing
    for name, val in _SPACING_TOKENS.items():
        lines.append(f"#let space-{name} = {val}pt")

    lines.append("")

    # Typography
    for role, spec in TYPOGRAPHY.items():
        lines.append(f"#let type-{role}-size = {spec['size_pt']}pt")

    return "\n".join(lines)

# ---------------------------------------------------------------------------
# Export: Python dict (for generators)
# ---------------------------------------------------------------------------

def to_python_dict() -> dict:
    """Return all tokens as a nested dict for programmatic use."""
    return {
        "colors": {
            **CCA_PALETTE,
            **DARK_PALETTE,
            "series": SERIES_COLORS,
        },
        "spacing": _SPACING_TOKENS,
        "typography": TYPOGRAPHY,
        "fonts": {
            "body": "Source Sans 3",
            "code": "Source Code Pro",
            "fallbacks": ["Helvetica Neue", "Arial", "sans-serif"],
        },
    }

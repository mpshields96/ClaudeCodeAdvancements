"""
CTX-5: Compaction Anchor Hook — PostToolUse

Writes a minimal COMPACT_ANCHOR.md every N tool calls, capturing the essential
session state that must survive context compaction. If Claude Code compacts the
context mid-session, this file is already on disk and Claude can re-read it to
restore awareness of the current task without parsing the full transcript.

What the anchor captures (machine-readable, from available data):
  - Session ID and context health (from state file)
  - Timestamp and tool call count
  - Last tool called

What it does NOT capture (requires Claude's reasoning):
  - Current task description (use /handoff for that)
  - Design decisions
  The anchor is intentionally minimal — it's a pointer, not a replacement for /handoff.

Environment variables (all optional):
  CLAUDE_CONTEXT_STATE_FILE     - State file path (default: ~/.claude-context-health.json)
  CLAUDE_CONTEXT_ANCHOR_PATH    - Anchor file path (default: ./.claude-compact-anchor.md)
  CLAUDE_CONTEXT_ANCHOR_EVERY   - Write every N tool calls (default: 10)
  CLAUDE_CONTEXT_ANCHOR_DISABLED - Set "1" to disable

Wire as PostToolUse hook (alongside meter.py):
  {
    "PostToolUse": [
      { "matcher": "", "hooks": [
          {"type": "command", "command": "python3 .../meter.py"},
          {"type": "command", "command": "python3 .../compact_anchor.py"}
      ]}
    ]
  }
"""
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path


DEFAULT_STATE_FILE = Path.home() / ".claude-context-health.json"
DEFAULT_ANCHOR_PATH = Path(".claude-compact-anchor.md")
DEFAULT_WRITE_EVERY = 10


# ---------------------------------------------------------------------------
# Pure functions
# ---------------------------------------------------------------------------

def load_state(state_path: Path) -> dict:
    """Load context health state. Returns empty dict on failure."""
    if not state_path.exists():
        return {}
    try:
        with open(state_path, "r", encoding="utf-8") as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError):
        return {}


def load_anchor_turn_count(anchor_path: Path) -> int:
    """
    Read the turn count stored in the anchor file.
    Returns 0 if file doesn't exist or can't be parsed.
    """
    if not anchor_path.exists():
        return 0
    try:
        content = anchor_path.read_text(encoding="utf-8")
        for line in content.splitlines():
            if line.startswith("<!-- turn_count:"):
                return int(line.split(":")[1].strip().rstrip(" -->"))
    except (OSError, ValueError):
        pass
    return 0


def should_write(turn_count: int, write_every: int) -> bool:
    """Write anchor on turn 0 (first write) or every write_every turns."""
    if write_every <= 0:
        return False
    return turn_count == 0 or (turn_count % write_every == 0)


def build_anchor_content(
    state: dict,
    tool_name: str,
    turn_count: int,
    session_id: str,
) -> str:
    """
    Build the compact anchor markdown content.
    Stays under 500 chars so it's cheap to re-read after compaction.
    """
    zone = state.get("zone", "unknown")
    pct = state.get("pct", 0)
    tokens = state.get("tokens", 0)
    window = state.get("window", 200000)
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    # Format window size for display
    window_str = f"{window // 1_000_000}M" if window >= 1_000_000 else f"{window // 1000}k"

    # Show threshold info if present in state (from adaptive_thresholds)
    thresholds = state.get("thresholds", {})

    lines = [
        "<!-- COMPACT ANCHOR — auto-generated by CTX-5 -->",
        f"<!-- turn_count: {turn_count} -->",
        "",
        "# Claude Code — Compact Anchor",
        f"_Updated: {now}_",
        "",
        "## Context Health",
        f"- Zone: **{zone}** ({pct:.0f}% of {window_str} tokens)",
        f"- Tokens used: ~{tokens:,}",
        f"- Session: `{session_id[:8]}...`" if session_id else "- Session: unknown",
        f"- Last tool: `{tool_name}`",
    ]
    if thresholds:
        lines.append(
            f"- Thresholds: yellow={thresholds.get('yellow', '?')}% "
            f"red={thresholds.get('red', '?')}% "
            f"critical={thresholds.get('critical', '?')}%"
        )
    # Autocompact proximity from meter state
    ac_proximity = state.get("autocompact_proximity")
    ac_pct = state.get("autocompact_pct")
    if ac_proximity is not None and ac_pct is not None:
        if ac_proximity <= 0:
            lines.append(f"- Auto-compact: **IMMINENT** (threshold={ac_pct}%)")
        else:
            lines.append(f"- Auto-compact: {ac_proximity:.0f} points away (threshold={ac_pct}%)")
    lines.extend([
        "",
        "## If you just resumed from compaction",
        "Re-read `SESSION_STATE.md` and any `HANDOFF.md` to restore task context.",
        "Run test suites before continuing: see `CLAUDE.md` for commands.",
    ])
    return "\n".join(lines) + "\n"


def write_anchor(anchor_path: Path, content: str) -> None:
    """Write anchor file atomically."""
    anchor_path.parent.mkdir(parents=True, exist_ok=True)
    tmp = anchor_path.with_suffix(".tmp")
    try:
        tmp.write_text(content, encoding="utf-8")
        tmp.replace(anchor_path)
    except OSError:
        try:
            tmp.unlink()
        except OSError:
            pass


# ---------------------------------------------------------------------------
# Hook entry point
# ---------------------------------------------------------------------------

def main() -> None:
    if os.environ.get("CLAUDE_CONTEXT_ANCHOR_DISABLED") == "1":
        sys.exit(0)

    state_file_str = os.environ.get("CLAUDE_CONTEXT_STATE_FILE", "")
    state_path = Path(state_file_str) if state_file_str else DEFAULT_STATE_FILE

    anchor_str = os.environ.get("CLAUDE_CONTEXT_ANCHOR_PATH", "")
    anchor_path = Path(anchor_str) if anchor_str else DEFAULT_ANCHOR_PATH

    write_every = int(os.environ.get("CLAUDE_CONTEXT_ANCHOR_EVERY", str(DEFAULT_WRITE_EVERY)))

    try:
        raw = sys.stdin.read()
        payload = json.loads(raw) if raw.strip() else {}
    except (json.JSONDecodeError, ValueError):
        payload = {}

    session_id = payload.get("session_id", "")
    tool_name = payload.get("tool_name", "")

    state = load_state(state_path)
    turn_count = state.get("turns", 0)

    if not should_write(turn_count, write_every):
        sys.exit(0)

    content = build_anchor_content(state, tool_name, turn_count, session_id)
    write_anchor(anchor_path, content)

    sys.exit(0)


if __name__ == "__main__":
    main()
